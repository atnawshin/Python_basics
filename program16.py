subjects = ["c","C++","JAVA","Python","Android","OS","TOC"]
print(subjects)
print(subjects[0])     #prints items starting from index 1
print(subjects[2:])
print(subjects[-1])     #prints last item
print(subjects[:-1])    ##prints items except last index

#finding an item from a list

print("Python" in subjects)
print("Swift" in subjects)
print("Swift" not in subjects)
print(subjects+["Swift",27]) #adding more items to the list
print(subjects*3) #multiple times shows the items of the list